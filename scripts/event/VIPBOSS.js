function init() {
    em.setProperty("state", "0");
    em.setProperty("leader", "true");
}

function setup(eim, leaderid) {
    em.setProperty("state", "1");
    em.setProperty("leader", "true");
    var eim = em.newInstance("VIPboss");
    var map = eim.setInstanceMap(940200506);
    map.resetFully();
    eim.getMapFactoryMap(940200506).killAllMonsters(false);
    var mob = em.getMonster(9600040);
    mob.getStats().setChange(true);
    mob.changeLevel(275);
    mob.getChangedStats().setOHp(9000);
    mob.setHp(9000);
    map.spawnMonsterOnGroundBelow(mob, new java.awt.Point(411, -2));
    eim.startEventTimer(60 * 1000 * 1); //20分钟
    return eim;
}

function playerEntry(eim, player) {
    var map = eim.getMapInstance(0);
    player.setReviveCount(reviveCount);
    player.changeMap(map, map.getPortal(0));
}

function changedMap(eim, player, mapid) {
    if (mapid != 940200506) {
        eim.unregisterPlayer(player);
        if (eim.disposeIfPlayerBelow(0, 0)) {
            em.setProperty("state", "0");
            em.setProperty("leader", "true");
        }
    }
}

function playerDisconnected(eim, player) {
    return 0;
}

function scheduledTimeout(eim) {
    eim.disposeIfPlayerBelow(100, 101050000);
    em.setProperty("state", "0");
    em.setProperty("leader", "true");
}

function playerExit(eim, player) {
    eim.unregisterPlayer(player);
    if (eim.disposeIfPlayerBelow(0, 0)) {
        em.setProperty("state", "0");
        em.setProperty("leader", "true");
    }
}

