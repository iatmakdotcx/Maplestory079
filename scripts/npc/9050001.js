function start() {
	status = -1;
	action(1, 0, 0);
}

function action(mode, type, selection) {

	if (mode == -1 || mode == 0) {
		cm.dispose();
		return
	} else {
		if (mode == 1)
			status++;
		else
			status--;
		if (status == 0) {
			if (cm.haveItem(5220040)) {
				cm.sendYesNo("欢迎参加本次系统活动。使用#b飞天猪的蛋#k你可获得异想不到的物品！系统会随机抽取物品,是否继续使用#b飞天猪的蛋#k进行抽奖？");
			} else {
				//cm.sendOk("欢迎参加本次系统活动。你确定有飞天猪的蛋吗？");
				cm.sendOk("快把#v5220040##b飞天猪的蛋#k找来吧!");
				cm.dispose();
			}
		} else if (status == 1){	
			

			for(var i = 1;i<=5;i++){
				if(cm.getPlayer().getInventory(Packages.client.inventory.MapleInventoryType.getByType(i)).isFull()){
					cm.sendOk("您至少应该让所有包裹都空出一格");
					cm.dispose();
					return;
				}
			}
		
			var chance = Math.floor(Math.random()*1000);
			var finalitem = Array();
			for(var i = 0 ;i<item.length;i++){
				if(item[i][1] >= chance){
					finalitem.push(item[i]);
				}
			}
			var ii = Packages.server.MapleItemInformationProvider.getInstance();
			if(finalitem.length != 0 || true){
				var random = new java.util.Random();
				var finalchance = random.nextInt(finalitem.length);
				var itemId = finalitem[finalchance][0];
				var quantity = finalitem[finalchance][2];
			        if(Packages.constants.GameConstants.getInventoryType(itemId).getType() == 1){
			        	var toDrop = ii.randomizeStats(ii.getEquipById(itemId)).copy();
				}
				else{
				 	 var toDrop = new Packages.client.inventory.Equip(itemId, 0, 0).copy();
				}
				Packages.server.MapleInventoryManipulator.addFromDrop(cm.getC(), toDrop, -1);
				if(Packages.constants.GameConstants.getInventoryType(itemId).getType() == 3){
					cm.sendGachaponMega("[飞天猪]","【飞天猪抽奖】中",toDrop,1)
				}
				cm.gainItem(5220040,-1);
				cm.getPlayer().saveToDB(true, false);
				cm.sendOk("感谢你帮我找回蛋。请收下我的礼物吧！\r\n\r\n#fUI/UIWindow.img/QuestIcon/4/0#\r\n\r\n#v"+itemId+"# x1");
				cm.dispose();
			} else {							
				cm.gainItem(5220040,-1);
				cm.gainItem(2022140,1);
				cm.getPlayer().saveToDB(true, false);
				cm.sendOk("欢迎参加本次系统活动。你确定有飞天猪的蛋吗？");
				cm.dispose();
			}
		}
	}
}
