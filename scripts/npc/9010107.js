function action(mode, type, selection) {
    if (status >= 1 && mode == 0) {
        cm.sendOk("快捷寻找组队按热键“O”赶快加入组队来挑战组队任务吧。");
        cm.dispose();
        return;
    }
    if (mode == 1)
        status++;
    else
        status--;

    if (status == 0) {
		
    if (cm.getPlayer().getClient().getChannel() != 2) {
        cm.sendOk("只能在频道2进行.");
        cm.dispose();
        return;
    }
	
	if(nowday != "二" && nowday != "四" && nowday != "六" && nowday != "日"){
		cm.sendOk("#b本活动只在 周 #r 2 4 6 7 #b开放");
		cm.dispose();
		return;
	}
            var pqtry = maxenter - cm.getPQLog(PQ);
			var oDate = new Date(); //实例一个时间对象；
			var hour = oDate.getHours(); //获取系统时，
			var min = oDate.getMinutes(); //分
			var se = oDate.getSeconds(); //秒
			var date1 = hour+" 时 "+min+" 分 ";
			if(19 < hour && hour < 20){
				if(min < 45 && min > 29){
					tagDate = true;
				}
				if(min < 56 && min >= 45){
					startDate = true;
				}
			}
            txt = "#b\r\n今天是 #r星期"+nowday+" #b本活动开放哦! #r当前时间 "+date1+"\r\n#gPS:请在7点30分--7点45分参与报名,挑战时间为30分钟\r\n#b注意:第一个报名的人在7点45分-7点55分期间须#r'确认入场'#b\r\n#r注意:奖励请于#b当天 8:30 - 9:00 领取 #r\r\n#b暴力熊远征队入口.是否报名?\r\n#L4##r查看人员#l\r\n#L3##r确定报名#l\r\n#L1##r确认入场#l\r\n#L5##r远征奖励#l";
			
			//test
			tagDate = true;startDate = true;
            
			cm.sendSimpleS(txt, 2);

    } else if (status == 1) {
		if (selection == 2){
			cm.warp(startmap);
			cm.dispose();
			return;
		}
		if (selection == 5){
			cm.dispose();
			cm.openNpc(9900000,"远征奖励");
			return;
		}
		if (selection == 4){
			
			var acc = cm.sql_Select("SELECT * FROM `team_boss`");
			
			var i = acc.length;
			if (i == 0) {
				cm.sendOk("没有人报名!");
				cm.dispose();
				return;
			}
			var ttt = "#b"
			for(var d = 0; d<i ;d++){
				ttt += (d+1)+" : " + acc[d].get("character") ;
				
				if(d==0){
					ttt+="#r         (队长)#b"
				}
				
				ttt +=  "\r\n";
			}
			
			cm.sendOk("当前报名人员有：\r\n"+ttt);
			cm.dispose();
			return;
		}
		if (selection == 3){//报名
			
			if(!tagDate){
				cm.sendOk("时间已过 无法报名");
				cm.dispose();
				return;
			}
			var acc = cm.sql_Select("SELECT * FROM `team_boss` WHERE `accid` = ?",  cm.getPlayer().getId());
			var i = acc.length;
			if (i == 0) {
				if(cm.sql_Insert("insert into team_boss (accId,damage,`character`) values ("+cm.getPlayer().getId()+",0,'"+ cm.getPlayer().getName()+"')")){
					cm.sendOk("您成功报名!");
				}
					else{
					cm.sendOk("出现错误!");
					}
			} else {
				cm.sendOk("您已经报名了!无需再次报名");
			}
			cm.dispose();
			return;
		}
        if (selection == 1) {
			
			var em = cm.getEventManager(eventname);
			
			var eim = cm.getDisconnected(eventname);
			
			if (em == null || open == false) {
				cm.sendSimple("配置文件不存在,请联系管理员。");
				cm.dispose();
			} else {
				
				if(!startDate){
					cm.sendOk("时间已过 无法进入");
					cm.dispose();
					return;
				}
				
				var acc = cm.sql_Select("SELECT * FROM `team_boss`");
				
				var i = acc.length;
				if (i == 0) {
					cm.sendOk("没有人报名,无法开启!");
					cm.dispose();
					return;
				}
				
				if(acc[0].get("accId") == cm.getPlayer().getId()){
					for(var d = i-1;d >= 0;d--){
						em.startInstance(cm.getPlayer().getMap().getCharacterById(acc[d].get("accId")), cm.getMap())
					}
				}else{
					cm.sendOk("#b需要队长 #r" + acc[0].get("character") + " #b才能进入");
					cm.dispose();
					return;
				}
			}
            
        } else if (selection == 2) {
            cm.warp(startmap, 0);
            cm.dispose();
        }
    } else if (mode == 0) {
        cm.dispose();
    }
}
