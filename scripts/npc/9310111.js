var status = 0;
//限制等级
var minLevel = 100; //最低等级
var maxLevel = 300;//最高等级
var channel = 0; //设置可以执行的频道

//限制人数
var minPlayers = 1;
var maxPlayers = 1;

//怪物最大等级设置
var moblevel = 255;

//副本开关 开启、true 关闭、false
var open = true;

//组队log记录
var PQ = '火焰狼';
//开始地图
var startmap = 101050000;
//配置文件名称
var eventname = "CareFirewolf";
//消耗的道具
var 道具 = 4033247;

//设置每日次数
var maxenter = 3;

var txt;
function start() {
    status = -1;
    action(1, 0, 0);
}
function action(mode, type, selection) {
    if (status >= 1 && mode == 0) {
        cm.sendOk("快捷寻找组队按热键“O”赶快加入组队来挑战组队任务吧。");
        cm.dispose();
        return;
    }
    if (mode == 1)
        status++;
    else
        status--;

    if (status == 0) {
        var pqtry = maxenter - cm.getPQLog(PQ);
        txt = "\t\t　 #b " + Icon[73][1] + " #r#e #fs16#火焰狼副本入口#fs12# #n#b " + Icon[74][1] + " #k\r\n\r\n";
        txt += Icon[41][1];
        for (var i = 0; i <= 59; i++) {
            txt += Icon[42][1];
        };
        txt += Icon[43][1] + "\r\n\r\n";

        txt += "　" + Icon[68][1] + "　#b推荐等级 #r230 - 300#b　　推荐人员 #r 1 - 1　　 " + Icon[68][1] + "#k\r\n\r\n";
        txt += "　" + Icon[68][1] + "　#b已进行普通模式 [ #r" + cm.getPQLog(PQ) + "#b ] 回合　剩余 [ #r" + pqtry + "#b ] 回合#k\r\n";
        txt += "　　　　　　　  #L1#" + Icon[15][1] + " #d开始挑战 " + Icon[15][1] + "#l#k\r\n\r\n\r\n";

        txt += Icon[41][1];
        for (var i = 0; i <= 59; i++) {
            txt += Icon[42][1];
        };
        txt += Icon[43][1] + "\r\n";
        cm.sendSimpleS(txt, 2);

    } else if (status == 1) {
        if (selection == 1) {
        
            if (cm.getParty() == null) { //判断组队
                cm.sendOk("你没有创建组队,无法入场。");
                cm.dispose();
            } else if (!cm.isLeader()) { // 判断组队队长
                cm.sendOk("请你们团队的队长和我对话。");
                cm.dispose();
            } else if (cm.getPQLog(PQ) >= maxenter) {
                cm.sendOk("你今天挑战次数已经用完了,请明天在来吧!");
                cm.dispose();
            } else if (!cm.allMembersHere()) {
                cm.sendOk("你的组队部分成员不在当前地图,请召集他们过来后在尝试。"); //判断组队成员是否在一张地图..
                cm.dispose();
			 
            } else {
                var party = cm.getParty().getMembers();
                var mapId = cm.getMapId();
                var next = true;
                var levelValid = 0;
                var inMap = 0;

                var it = party.iterator();
                while (it.hasNext()) {
                    var cPlayer = it.next();
                    if (cPlayer.getLevel() >= minLevel && cPlayer.getLevel() <= maxLevel) {
                        levelValid += 1;
                    } else {
                        cm.dispose();
                        next = false;
                    }
                    if (cPlayer.getMapid() == mapId) {
                        inMap += 1;
                    }
                }
                if (party.size() > maxPlayers || inMap < minPlayers) {
                    next = false;
                }
                if (next) {
                    var em = cm.getEventManager(eventname);
                    if (em == null || open == false) {
                        cm.sendSimple("配置文件不存在,请联系管理员。");
                    } else {
                        var prop = em.getProperty("state");
                        if (prop == null || prop.equals("0")) {
                            //cm.setPQLog(PQ);
							cm.gainMembersPQ(PQ, 1);
							cm.worldSpouseMessage(0x15,"[副本消息]:   玩家 "+ cm.getChar().getName() +" 带领他的队伍一起去挑战火焰狼了!!! ");//副本提示
                            em.startInstance(cm.getParty(), cm.getMap(), "+ moblevel +");
                        } else {
                            cm.sendSimple("已经有队伍在进行了,请换其他频道尝试。");
                        }
                        cm.dispose();
                    }
                } else {
                    cm.sendOk("组队成员 " + minPlayers + " 人以上 " + maxPlayers + "人 以下 所有成员等级 " + minLevel + " 以上 " + maxLevel + " 以下才可以入场。");
                    cm.dispose();
                }
            }
        } else if (selection == 2) {
            cm.warp(startmap, 0);
            cm.dispose();
        }
    } else if (mode == 0) {
        cm.dispose();
    }
}